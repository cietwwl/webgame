package com.pwrd.war.gameserver.interactive.domain;

public class SkillUnit {
	
	private String skillSn;
	
	private int skillRank;

	public String getSkillSn() {
		return skillSn;
	}


	public void setSkillSn(String skillSn) {
		this.skillSn = skillSn;
	}


	public int getSkillRank() {
		return skillRank;
	}


	public void setSkillRank(int skillRank) {
		this.skillRank = skillRank;
	}



}
